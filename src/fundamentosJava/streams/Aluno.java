package fundamentosJava.streams;

public class Aluno {

	private final String nome;
	private final Double nota;


	public Aluno(String nome, Double nota) {
		super();
		this.nome = nome;
		this.nota = nota;
	}

	public String getNome() {
		return nome;
	}

	public Double getNota() {
		return nota;
	}

}
